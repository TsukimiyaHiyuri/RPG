// 壁の判定
bool WorldMap::judgeWall(int x, int y) {
	if (x >= 0 && y >= 0 && x < this->height && y < this->width) {
		if (this->mapSea[x][y] == 4 && (this->mapGround[x][y] != 3 && this->mapGround[x][y] != 4 && this->mapGround[x][y] != 5 && this->mapBridge[x][y] != 28 && this->mapBridge[x][y] != 31 && this->mapBridge[x][y] != 34)) {
			return true;
		}

		switch (this->mapGround[x][y]) {
		case 15: return true;
		case 16: return true;
		case 17: return true;
		case 27: return true;
		case 29: return true;
		case 30: return true;
		case 32: return true;
		case 33: return true;
		case 35: return true;
		}
	}

	return this->judgeWallNPC(x, y);
}

