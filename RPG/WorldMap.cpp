Map *WorldMap::changeMap(Player *player, Map *nowMap, Map *list[]) {
	int x = player->getx();
	int y = player->gety();

	if (x == 25 && y == 8) {
		player->setPlayer(14, 19);
		return list[Town];
	}
	return nowMap;
}

