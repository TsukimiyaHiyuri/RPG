// 壁の判定
bool TownMap::judgeWall(int x, int y) {

	if (x >= 0 && y >= 0 && x < this->height && y < this->width) {
		if (this->mapSea[x][y] == 4 && (this->mapGround[x][y] != 3 && this->mapGround[x][y] != 4 && this->mapGround[x][y] != 5 && this->mapBridge[x][y] != 28 && this->mapBridge[x][y] != 31 && this->mapBridge[x][y] != 34)) {
			return true;
		}

		switch (this->mapGround[x][y]) {
		case 15: return true;
		case 16:
			if (this->mapBridge[x][y] != 28 && this->mapBridge[x][y] != 31 && this->mapBridge[x][y] != 34) {
				return true;
			}
			break;
		case 17: return true;
		}

		switch (this->mapBridge[x][y]) {
		case 27: return true;
		case 29: return true;
		case 30: return true;
		case 32: return true;
		case 33: return true;
		case 35: return true;
		}
	}

	return this->judgeWallNPC(x, y);
}

//	次のマップに移動するかを監視する
//	移動する前に次のマップのどの座標に主人公を描画するかを描いておく
Map *TownMap::changeMap(Player *player, Map *nowMap, Map *list[]) {
	int x = player->getx();
	int y = player->gety();

	if (x == 14 && y == 21) {
		player->setPlayer(25, 10);
		return list[World];
	}

	if (x == 25 && y == 8) {
		player->setPlayer(14, 20);
		return list[Snow];
	}

	if (x == 14 && y == 8) {
		player->setPlayer(14, 31);
		return list[Maze];
	}
	return nowMap;
}

